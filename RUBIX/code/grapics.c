void mouse_handler(int btn, int state, int x, int y) {
	if (btn == GLUT_LEFT_BUTTON && state == GLUT_DOWN) {
		dx[0] = x;
		dy[0] = y;
		if ((x>1000 * (sizeW / 1200) && x<1180 * (sizeW / 1200)) && (y>515 * (sizeH / 800) && y<600 * (sizeH / 800))) {
			startgame();
		}
		if ((x>1000 * (sizeW / 1200) && x<1180 * (sizeW / 1200)) && (y>640 * (sizeH / 800) && y<730 * (sizeH / 800))) {
			stopgame();
		}
		if ((x>40 * (sizeW / 1200) && x<105 * (sizeW / 1200)) && (y>70 * (sizeH / 800) && y<95 * (sizeH / 800))) {
			if (htoggle == 0)
				htoggle = 1;
			else
				htoggle = 0;
		}
		if (finished == 1)
			finished = 0;
	}
	if (btn == GLUT_LEFT_BUTTON && state == GLUT_UP) {
		dx[1] = x;
		dy[1] = y;
		p3[0] = (dx[1] - dx[0]) / 500;
		p3[1] = (dy[1] - dy[0]) / 500;
		theta[0] += p3[0] * 50;
		theta[1] += p3[1] * 50;
		if (theta[0]>360)
			theta[0] -= 360;
		if (theta[1]>360)
			theta[1] -= 360;
		if (theta[0]<-360)
			theta[0] += 360;
		if (theta[1]<-360)
			theta[1] += 360;
	}

}
